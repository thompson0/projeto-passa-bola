export const matchesService = {
  getMatches: () => {
    return matchesData;
  },
  
  getMatchById: (id) => {
    return matchesData.find(match => match.id === parseInt(id)) || null;
  },
  
  confirmMatch: (id) => {
    const match = matchesData.find(match => match.id === parseInt(id));
    
    if (!match) {
      return { success: false, message: 'Partida não encontrada.' };
    }
    
    if (match.status !== 'Agendado') {
      return { success: false, message: 'Não é possível confirmar presença em uma partida que não está agendada.' };
    }
    
    // Em um ambiente real, aqui seria feita a atualização no banco de dados
    match.confirmed = true;
    
    return { 
      success: true, 
      message: 'Presença confirmada com sucesso!',
      data: match
    };
  }
};